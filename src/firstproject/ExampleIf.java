package firstproject;


public class ExampleIf {
      public static void main (String[] args){
          
          int x = 100;
          int y = 1000;
          
          if (x < y){
              System.out.println("x is less than y");
              
          }else if (x == y){
              System.out.println("x is equal to y");
              
          }else {
              System.out.println("x is greater than y");
              
          }
      }
    
}
